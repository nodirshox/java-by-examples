public class Variable {
    public static void main(String[] args) {        
        // string
        String name = "Nodirbek Ergashev";
        System.out.println(name);

        // integer, declare first and initialize after
        int age;
        age = 23;
        System.out.println(age);

        // floating
        float myBalance = 1.22f;
        System.out.println(myBalance);

        // boolean
        boolean isOpen = true;
        System.out.println(isOpen);

        // char (store single character value like "a", "r", ...) (*with single quote, NOT double)
        char myLetter = 'd';
        System.out.println(myLetter);
    }
}